package org.xper.allen.nafc.blockgen;

public class PngBasePaths {
	public String generatorPngPath;
	public String experimentPngPath;
	public String specPath;

	public PngBasePaths() {
	}

	public PngBasePaths(String generatorPngPath, String experimentPngPath, String specPath) {
		super();
		this.generatorPngPath = generatorPngPath;
		this.experimentPngPath = experimentPngPath;
		this.specPath = specPath;
	}
}